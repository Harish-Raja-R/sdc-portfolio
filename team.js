/* ===== HELPERS ===== */
function slugify(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
}

// Flat lookup map: slug → {member, divisionTitle}
const MEMBER_MAP = {};
DIVISIONS.forEach(div => {
  div.members.forEach(m => {
    MEMBER_MAP[slugify(m.name)] = { member: m, divisionTitle: div.title };
  });
});

/* ===== DOM READY ===== */
document.addEventListener('DOMContentLoaded', () => {

  const section = document.getElementById('membersSection');
  const photoEl = document.getElementById('hoverPhoto');
  const imgEl = document.getElementById('hoverImg');

  /* ---- Build DOM ---- */
  let globalIdx = 1;

  DIVISIONS.forEach((div, di) => {
    const block = document.createElement('div');
    block.className = 'member-division reveal';

    const header = document.createElement('div');
    header.className = 'division-header';
    header.innerHTML = `
      <span class="division-num">${String(di + 1).padStart(2, '0')} /</span>
      <h2 class="division-title">${div.title}</h2>
      <span class="division-dot" aria-hidden="true"></span>
    `;
    block.appendChild(header);

    const list = document.createElement('ul');
    list.className = 'member-list';
    list.setAttribute('role', 'list');

    div.members.forEach(member => {
      const slug = slugify(member.name);
      const li = document.createElement('li');
      li.className = 'member-row';
      li.setAttribute('id', 'member-' + slug);
      li.setAttribute('title', 'Double-click to view profile');

      // Left: index
      const idxEl = document.createElement('span');
      idxEl.className = 'member-index';
      idxEl.textContent = String(globalIdx).padStart(2, '0');
      idxEl.setAttribute('aria-hidden', 'true');

      // Centre: name + year
      const centre = document.createElement('div');
      centre.className = 'member-row-centre';

      const nameEl = document.createElement('span');
      nameEl.className = 'member-name';
      nameEl.textContent = member.name;

      const yearEl = document.createElement('span');
      yearEl.className = 'member-year';
      yearEl.textContent = member.year;

      centre.append(nameEl, yearEl);

      // Right: role + hint + links
      const right = document.createElement('div');
      right.className = 'member-row-right';

      const roleEl = document.createElement('span');
      roleEl.className = 'member-role';
      roleEl.textContent = div.title;

      const hintEl = document.createElement('span');
      hintEl.className = 'member-hint';
      hintEl.textContent = 'double-click';

      const linksEl = document.createElement('div');
      linksEl.className = 'member-links';
      if (member.linkedin) linksEl.appendChild(mkLink(member.linkedin, 'LinkedIn', member.name + ' LinkedIn'));
      if (member.instagram) linksEl.appendChild(mkLink(member.instagram, 'Instagram', member.name + ' Instagram'));
      if (member.website) linksEl.appendChild(mkLink(member.website, 'Faculty Page', member.name + ' Faculty Page'));

      right.append(roleEl, hintEl, linksEl);
      li.append(idxEl, centre, right);

      // Single hover → show/hide photo
      li.addEventListener('mouseenter', () => showPhoto(member.img, member.name));
      li.addEventListener('mouseleave', hidePhoto);

      // Double-click → open member profile page in new tab
      li.addEventListener('dblclick', (e) => {
        e.preventDefault();
        window.open(`members/profile.html?m=${slug}`, '_blank');
      });

      list.appendChild(li);
      globalIdx++;
    });

    block.appendChild(list);
    section.appendChild(block);
  });

  /* ---- Hover photo (fixed center, no mouse tracking) ---- */
  function showPhoto(src, alt) {
    if (!src) return;
    imgEl.src = src;
    imgEl.alt = alt;
    photoEl.classList.add('is-visible');
  }
  function hidePhoto() {
    photoEl.classList.remove('is-visible');
  }

  /* ---- Scroll reveal ---- */
  const observer = new IntersectionObserver(entries => {
    entries.forEach(e => {
      if (e.isIntersecting) { e.target.classList.add('is-visible'); observer.unobserve(e.target); }
    });
  }, { threshold: 0.05 });
  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

  /* ---- Helpers ---- */
  function mkLink(href, label, ariaLabel) {
    const a = document.createElement('a');
    a.className = 'member-link';
    a.href = href; a.target = '_blank'; a.rel = 'noopener noreferrer';
    a.textContent = label;
    a.setAttribute('aria-label', ariaLabel);
    a.addEventListener('click', e => e.stopPropagation());
    return a;
  }

}); // end DOMContentLoaded